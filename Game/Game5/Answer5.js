function btn01() { 
  document.getElementById("btn01").addEventListener("click",function(){
  swal("請輸入答案", {
    content: "input",
  })
  .then((value) => {
    if (value == "仙楂餅"){
      swal("恭喜你答對了","","success");
      document.getElementById('Yes01').innerHTML = '答案：仙楂餅';
    }else if(value == ""){
      swal("答案不能為空白","","warning");
    }else{
      swal("不好意思你答錯了","","error");
    }
  });
});
}
