function btn01() { 
  document.getElementById("btn01").addEventListener("click",function(){
  swal("請輸入答案", {
    content: "input",
  })
  .then((value) => {
    if (value == "大象"){
      swal("恭喜你答對了","","success");
      document.getElementById('Yes01').style.visibility = 'visible';
    }else if(value == ""){
      swal("答案不能為空白","","warning");
    }else{
      swal("不好意思你答錯了","","error");
    }
  });
});
}
